SET SQL_MODE="NO_AUTO_VALUE_ON_ZERO";
START TRANSACTION;
SET time_zone = "+00:00";

CREATE TABLE CUSTOMER(
        FNAME VARCHAR(10) NOT NULL,
        LNAME VARCHAR(10) NOT NULL,
        PHONENO DECIMAL(11,0) NOT NULL,
        RESERVATION_NO INT PRIMARY KEY AUTO_INCREMENT,
        PAYMENT INT(10)
 );
    
CREATE TABLE BOOK(
        SNO INT PRIMARY KEY AUTO_INCREMENT,
        ROOMTYPE VARCHAR(20),
        RESERVATION_NO INT REFERENCES CUSTOMER(RESERVATION_NO),
        NUMBER_OF_ROOMS DECIMAL(10,0),
        CHECKIN DATE NOT NULL DEFAULT CURRENT_TIMESTAMP, 
        CHECKOUT DATE NOT NULL DEFAULT CURRENT_TIMESTAMP
         
);

CREATE TABLE HOTEL(
        SNO INT PRIMARY KEY AUTO_INCREMENT,
        HNAME VARCHAR(20) ,
        HADDRESS VARCHAR(100) NOT NULL, 
        CONTACTNO DECIMAL(22,0) NOT NULL,
        EMAIL VARCHAR(100) NOT NULL,
        HOTEL_INFO VARCHAR(500) NOT NULL
);

CREATE TABLE EMPLOYEE(
        SNO INT PRIMARY KEY AUTO_INCREMENT,
    FNAME VARCHAR(20) ,
    LNAME VARCHAR(20) ,
    PHONENO DECIMAL(10,0) NOT NULL,
    SEX CHAR(3),
   AGE DECIMAL(10,0) NOT NULL,
   SALARY DECIMAL(10,0) NOT NULL,
   DESIGNATION VARCHAR(50) NOT NULL
);

CREATE TABLE AVAIL(
        ROOMID VARCHAR(10),
        ROOMTYPE VARCHAR(20) REFERENCES BOOK(ROOMTYPE) ON DELETE CASCADE,
        AVAILABILITY_NO VARCHAR(20) NOT NULL,
        PRICE INT(10)
);

